#include <stdio.h>
int main()
{
	int n, c=0, max;
	
	while(c<10)
	{
		c=c+1;
		printf("inserisci un numero:\n ");	
		scanf("%d",&n);
		
		if(c==1 || n>max)	
			max=n;	
	}
	
	printf("il maggiore e' %d",max);
	return 0;
}

